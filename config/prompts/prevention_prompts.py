# 종합 프롬프트 딕셔너리
PREVENTION_PROMPTS = {
    "basic_education": BASIC_EDUCATION_PROMPT,
    "scenario_training": SCENARIO_EDUCATION_PROMPT,
    "quiz_generation": QUIZ_GENERATION_PROMPT,
    "personalized": PERSONALIZED_EDUCATION_PROMPT,
    "family_education": FAMILY_EDUCATION_PROMPT,
    "advanced": ADVANCED_EDUCATION_PROMPT,
    "trauma_care": TRAUMA_CARE_PROMPT
}

# 프롬프트 선택 함수
def get_prevention_prompt(prompt_type: str, user_profile: dict = None) -> str:
    """사용자 프로필에 따른 적절한 예방교육 프롬프트 반환"""
    
    base_prompt = PREVENTION_PROMPTS.get(prompt_type, PREVENTION_PROMPTS["basic_education"])
    
    # 사용자 프로필이 있으면 개인화
    if user_profile:
        age_group = user_profile.get("age_group", "adult")
        experience_level = user_profile.get("experience_level", "beginner")
        
        if experience_level == "advanced":
            return PREVENTION_PROMPTS["advanced"]
        elif user_profile.get("has_family", False):
            return PREVENTION_PROMPTS["family_education"]
        elif user_profile.get("victim_experience", False):
            return PREVENTION_PROMPTS["trauma_care"]
    
    return base_prompt

# 교육 컨텐츠 생성 함수
def create_lesson_content(topic: str, difficulty: str = "beginner") -> str:
    """주제와 난이도에 따른 교육 컨텐츠 생성 프롬프트"""
    
    base_prompt = f"""
다음 주제에 대해 {difficulty} 수준의 보이스피싱 예방교육 컨텐츠를 만드세요:

주제: {topic}
난이도: {difficulty}

## 포함할 내용
1. 개념 설명 (이해하기 쉽게)
2. 실제 사례 (익명화된)
3. 대응 방법 (구체적이고 실용적)
4. 체크리스트 (기억하기 쉽게)
5. 추가 팁 (생활 밀착형)

## 구성 방식
- 친근하고 이해하기 쉬운 언어
- 단계별 설명
- 시각적 요소 고려 (이모지 활용)
- 실천 가능한 조언

## 목표
학습 후 실제 상황에서 적용할 수 있는 실용적 지식 제공
"""
    
    return base_prompt

# 퀴즈 생성 함수
def create_quiz_prompt(topic: str, difficulty: str, question_count: int = 5) -> str:
    """주제별 퀴즈 생성 프롬프트"""
    
    return f"""
{topic}에 대한 {difficulty} 수준의 보이스피싱 예방 퀴즈를 {question_count}문제 만드세요.

## 퀴즈 요구사항
1. 실용적 지식 평가
2. 4지선다형
3. 명확한 해설 포함
4. 실생활 연결

## 문제 유형 분배
- 개념 이해: 40%
- 상황 판단: 40%  
- 대응 방법: 20%

## 난이도별 특징
- beginner: 명확한 정답, 기본 개념
- intermediate: 판단 필요, 애매한 상황
- advanced: 복합 상황, 미묘한 차이

각 문제마다 다음 정보 포함:
- 문제 (명확하고 구체적)
- 선택지 4개 (그럴듯한 오답 포함)
- 정답 번호
- 해설 (왜 정답인지, 오답은 왜 틀렸는지)
"""